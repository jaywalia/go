// 51 number parsing

package main

import "strconv"
import "fmt"

func main() {
	p := fmt.Println

	p("--------------------------------------------")
	f, _ := strconv.ParseFloat("1.234", 64)
	p(f)
	p("--------------------------------------------")
	i, _ := strconv.ParseInt("1234", 0, 64)
	p(i)
	p("--------------------------------------------")
	d, _ := strconv.ParseInt("0x1c8", 0, 64)
	p(d)
	p("--------------------------------------------")
	u, _ := strconv.ParseUint("789", 0, 64)
	p(u)
	p("--------------------------------------------")
	k, _ := strconv.Atoi("135")
	p(k)
	p("--------------------------------------------")
	_, e := strconv.Atoi("NAN")
	p(e)
	p("--------------------------------------------")

}
